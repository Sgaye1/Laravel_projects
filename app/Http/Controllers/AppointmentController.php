     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
        'doctor'=>'required','name'=>'required',
        'phone'=>'required','username'=>'required',
        'message'=>'required','date'=>'required','time'=>'required' ]);


        $appointment = Appointment::create($request->all());
        Mail::to($request->username)->send(new BookAppointment($appointment));
        return redirect()->route('appointment.create')->with('message','Email Send');
    }

